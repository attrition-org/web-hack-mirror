<center><b>
Установка счетчика количества посетителей:</b>
</center>
<br>
В своей директории Вы можете увидеть папку cgi-bin в которой вы можете размещать необходимые вам cgi и pl файлы, а так же два файла.
Первый .htaccess - обеспечивает работу счетчика
в вашей директории. Второй index.count - хранит в себе цифру посетителей Вашей страницы. 
Если Вы хотите установить счетчик не только на странице index.htm/html, но и на любой другой вам необходимо создать новый файл в вашей директории (или переименовать имеющийся) на name.count, где name - имя файла html в котором вы хотите установить счетчик.
Если вы хотите установить счетчик в субдиректории (файл html находится в папке в вашей директории) вам необходимо скопировать в эту директорию файл .htaccess и создать там файл name.count.<br>
<br>
Коды для вставки в html страницы:<br>
Для запуска счетчика на вашей странице вам необходимо вставить
следующий код в том месте где вы хотите видеть счетчик.<br>
<center><B>Текстовый счетчик:</B></center>
<font face="MS Sans Serif, Arial Cyr" color=#000000 size="2">

2235
</font><br>
