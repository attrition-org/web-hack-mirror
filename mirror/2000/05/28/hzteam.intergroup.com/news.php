<u>Футболка:</u><br>
 ~ 100 рублей (150 с пересылкой)<br>
<u>Кружка:</u><br>
 ~ 100 рублей (150 с пересылкой)<br>
<u>Комплект (футболка + кружка):</u><br>
 ~ 190 рублей (240 с пересылкой)<br><br>
