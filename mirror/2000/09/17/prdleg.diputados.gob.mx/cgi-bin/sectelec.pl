<B><FONT SIZE=4><CENTER> NO A LA PRIVATIZACIÓN DE LA INDUSTRIA ELÉCTRICA NACIONAL </CENTER></P></B>
<P ALIGN="JUSTIFY">
Declaración del Grupo Parlamentario del Partido de la Revolución Democrática en la Cámara de Diputados del Congreso de la Unión<BR>
<BR>
El Grupo Parlamentario del Partido de la Revolución Democrática en la Cámara de Diputados hace pública su postura frente a la iniciativa presentada por el Presidente de la República para modificar los artículos 27 y 28 constitucionales, con el objeto de suprimir el servicio público de energía eléctrica y privatizar la industria eléctrica nacional.<BR>
<BR>
¿Está en  crisis el sector eléctrico?<BR>
El Presidente Ernesto Zedillo pretende sorprender al pueblo presentando en forma alarmista la necesidad de que la industria eléctrica crezca en los próximos años a una tasa anual de seis por ciento. Históricamente, el sector eléctrico nacionalizado registra por décadas (1950-1980) crecimientos mayores al ocho por ciento anual. Ese reto ha podido superarse, aún en condiciones más difíciles que las actuales, debido a la decisión de mantener un proyecto nacional para atender las necesidades básicas de la gran mayoría de la población y el desarrollo económico, así como por la participación de los trabajadores, técnicos y profesionistas de la industria eléctrica.<BR>
<BR>
La Comisión Federal de Electricidad, que genera el 90 por ciento de la energía eléctrica y distribuye el 80 por ciento, tiene índices de productividad comparables con las mejores experiencias internacionales: 85 por ciento de disponibilidad en centrales generadoras, tiempo de interrupción usuario de 235 minutos, eficiencia en centrales termoeléctricas de 34 por ciento; todo el sector eléctrico proporciona servicio al 95% de la población.<BR>
<BR>
De acuerdo con los documentos oficiales, de los 13 000 Megawatts (Mw) requeridos en los próximos seis años (que apenas el año pasado la Secretaría de Energía informaba que cubrirían las necesidades de una década), mediante los esquemas aplicados de inversión financiada se han captado los recursos económicos para la instalación de 5 800 Mw, por lo que no existe emergencia en las necesidades de capacidad de producción, ni carencia de los instrumentos para la captación de recursos.<BR>
<BR>
Nuestra conclusión es que no existe crisis en el sector eléctrico o necesidad de una reforma que implique la privatización de los activos o servicios de la Comisión Federal de Electricidad (CFE) o de Luz y Fuerza del Centro (LFC).<BR>
<BR>
<BR>
¿Se afecta el gasto social con la operación del sector eléctrico nacional?<BR>
<BR>
La CFE sufraga su propio gasto corriente con los ingresos por ventas, cubre el subsidio actual de algunas tarifas (30 por ciento de sus ingresos) y aporta recursos al fisco para las transferencias a Luz y Fuerza del Centro. Por tanto, la operación del sector no es una carga al Presupuesto.<BR>
<BR>
En cambio, la reforma plantea que el subsidio sea directo a los usuario con cargo al Presupuesto y que el Estado se haga cargo de las actividades no rentables por cuenta del Presupuesto, tales como la electrificación rural y la operación de centrales de respaldo.<BR>
<BR>
La CFE opera con finanzas sanas. Una proyección de los resultados últimamente obtenidos, permite estimar que CFE podría cubrir con sus propios recursos cuando menos el 60 por ciento de las inversiones requeridas, siempre que contara con autonomía presupuestal.<BR>
<BR>
El gobierno ha venido restringiendo de manera irresponsable las asignaciones presupuestales de la CFE como falsa respuesta a la crisis de las finanzas públicas a que éste mismo ha conducido. Los altos gastos en el Fobaproa y en otros proyectos gubernamentales no son responsabilidad de la industria eléctrica ni del sector público de la economía sino de los innegables errores del Presidente Zedillo en la conducción de la política económica.<BR>
<BR>
<BR>
¿Con la privatización se obtendrían menores precios al consumidor?<BR>
<BR>
Como resultado del buen desempeño de la Comisión Federal de Electricidad, las tarifas eléctricas en México son de las más bajas del mundo.<BR>
<BR>
Los generadores privados y los concesionarios en transmisión y distribución tendrían que incluir un mayor margen de utilidad en las tarifas para ubicarlas en el nivel internacional.<BR>
<BR>
La desintegración de la industria en varias empresas incrementaría los gastos fijos y administrativos.<BR>
<BR>
En transmisión y distribución no existiría competencia dentro de una misma región sino que el servicio estararía en manos de monopolios privados.<BR>
<BR>
No habría subsidios a las tarifas por parte de las empresas privadas.<BR>
<BR>
Lo anterior  permite anticipar que, con la privatización de la industria eléctrica, debe de esperarse  un aumento importante en las tarifas.<BR>
<BR>
<BR>
¿Qué nos dice la experiencia internacional?<BR>
<BR>
La reforma privatizadora del Presidente de la República toma como referencia la experiencia en otros países, pero muy poco la experiencia de nuestro propio país, a pesar de que México tiene más aciertos que otros países.<BR>
<BR>
Las naciones que han experimentado mejoras con la privatización de la industria eléctrica (Argentina) son los que tenían sistemas eléctricos muy deficientes, lo que no es el caso de México.<BR>
<BR>
En los países con sistemas operando satisfactoriamente, la privatización no ha traído los beneficios esperados salvo, tal vez, para los grandes consumidores.<BR>
<BR>
Los países que han vendido los activos fijos de propiedad pública lo han hecho por debajo de su valor de reposición, convirtiendo la privatización en un negocio directo. Introducir en el mercado activos que no han sido históricamente conformados para ser vendidos genera una distorsión que inevitablemente redunda en pérdidas para el dueño de dichos activos industriales, es decir, la Nación, tal como lo demuestra toda la experiencia mundial.<BR>
<BR>
<BR>
¿Cuál sería la situación de los trabajadores?<BR>
<BR>
Como ha sucedido con otras privatizaciones en nuestro país y con la del sector eléctrico en otros países, una elite de empresarios resulta altamente beneficiada a costa del deterioro de las condiciones de vida de los trabajadores, ya sea por la disminución en sus percepciones o por los despidos masivos.<BR>
<BR>
El Partido de la Revolución Democrática, preocupado por que el país mantenga su soberanía en materia de energéticos y que la explotación de estos recursos sea para beneficio de la nación, propone las siguientes líneas para fortalecer al sector eléctrico, sin que sean necesarias modificaciones constitucionales:<BR>
<BR>
1. Preservar el principio de servicio público de energía eléctrica que garantiza el abasto general independientemente de los niveles de ingreso de los consumidores.<BR>
<BR>
2. Mantener la facultad del Estado de la planeación de largo plazo sobre el crecimiento, las fuentes de generación y de localización de los medios de producción, para salvaguardar el uso eficiente de los recursos naturales y la preservación del medio ambiente.<BR>
<BR>
3. Preservar, con autonomía presupuestal, empresas públicas integradas para la generación, transmisión y distribución de energía, conservando en propiedad pública el parque de generación actual, la red de transmisión y la distribución.<BR>
<BR>
4. Que la autonomía de las empresas públicas implique compromisos para mantener un suministro seguro y de calidad de energía eléctrica, con un régimen tributario similar al de cualquier empresa.<BR>
<BR>
5. Introducción de esquemas de competencia en la generación, donde las centrales generadoras estatales tendrían el mismo tratamiento que el resto de los generadores.<BR>
<BR>
6. Establecer una política de subsidios desconectada de las finanzas de las empresas públicas y asumidas adecuadamente en las políticas sociales del gobierno orientados a quien realmente lo requiere.<BR>
<BR>
7. Establecer un plan de rehabilitación que permita gradualmente, pero dentro de un período establecido, reducir el subsidio que se otorga al organismo Luz y Fuerza del Centro.<BR>
<BR>
El Presidente Ernesto Zedillo pretende engañar al pueblo mexicano al presentar como inevitable la privatización y extranjerización de la industria eléctrica nacional. El dogmatismo ideológico de que todo lo privado es mejor que lo público, independientemente del sector económico de que se trate, ha traído graves consecuencias para México y está siendo rebatido en el mundo entero.<BR>
Asimismo, el Presidente Zedillo pretende aprovechar la simpatía del Partido Acción Nacional por toda clase de privatizaciones y por la eliminación total del sector público de la economía para lograr las dos terceras partes de los votos en las cámaras del Congreso de la Unión que son necesarias para modificar la Constitución del país. Sin embargo, el titular del Ejecutivo no explica cómo se llevaría a cabo la privatización, la cual le correspondería al próximo gobierno, ni presenta un análisis de las consecuencias que ésta tendría para el desarrollo de la industria nacional y las necesidades de consumo de la población.<BR>
La propuesta privatizadora es por completo irresponsable y, de ser admitida, abriría el camino para la entrega del petróleo, largamente ambicionado por extranjeros y nacionales asociados a éstos.<BR>
La empresa pública mexicana debe desarrollarse, sobreponerse a la corrupción y ser tratada por los poderes Ejecutivo y Legislativo como instrumento del desarrollo y fuente de riqueza nacional. Este es el objetivo del Partido de la Revolución Democrática.<BR>
<BR>Palacio Legislativo, 10 de febrero de 1999.<BR>
