<B><FONT SIZE=4><CENTER> <BR>PROPUESTA DEL PARTIDO DE LA REVOLUCIÓN DEMOCRÁTICA SOBRE EL PRESUPUESTO DE EGRESOS DE LA FEDERACIÓN<br>
</CENTER></P></B>
<br>
<br>
<BLOCKQUOTE>AL PUEBLO DE MÉXICO:<br>
<br><P ALIGN="JUSTIFY">
El Grupo Parlamentario del Partido de la Revolución Democrática presenta su propuesta presupuestal para 1999, después de haber analizado los diversos planteamientos hechos por las comisiones de la Cámara de Diputados, los otros grupos parlamentarios de oposición y el trabajo realizado por el Comité Ejecutivo Nacional del PRD.<br>
<br>
INGRESOS:<br>
1. El PRD no admite la propuesta del Presidente Ernesto Zedillo para crear un nuevo impuesto al servicio telefónico del 15 por ciento y que representaría unos 11 mil millones de pesos de ingresos adicionales.<br>
2. El PRD no admite la propuesta gubernamental de incrementar el precio de la gasolina durante 1999 por considerar que el aumento de este año ya fue realizado el pasado 14 de noviembre en un 15 por ciento, lo cual implica una caída de ingresos previstos de unos 10 mil millones.<br>
3. El PRD considera que podría calcularse la caída del precio de la mezcla petrolera mexicana en unos 15 mil millones de pesos adicionales para 1999.<br>
4.  Para resarcir sólo parcialmente la posible caída de los ingresos anteriores, se procedería a:<br>
a.  Establecer el pago de derechos para la regularización de los automóviles de origen extranjero que ya se encuentran dentro del territorio nacional, lo cual ayudaría a resolver este grave problema, lo que implicaría aproximadamente mil 750 millones de pesos.<br>
b. Cobrar aranceles sobre los excedentes de importación de productos agropecuarios que se encuentren por arriba de los cupos previstos en el Tratado de Libre Comercio y que hasta la fecha no se han cobrado por decisión unilateral del poder Ejecutivo, que implicaría aproximadamente dos mil millones de pesos.<br>
c. Utilizar el llamado subejercicio de 1998, por dos mil millones de pesos que el gobierno federal hizo público recientemente.<br>
<br>
AHORROS:<br>
1. Se cancelaría totalmente la erogación destinada a "salvamento financiero", principalmente para Fobaproa, propuesto por el gobierno en 25 mil 145 millones de pesos.<br>
2. Se programarían 18 mil millones menos para el pago del servicio de la deuda pública, pues el proyecto del gobierno está sobreestimado con el propósito previsible de usar esta cantidad para un mayor apoyo al Fobaproa.<br>
3. Se recortarían 18 mil 273 millones de pesos de gastos innecesarios del gobierno federal de los diversos ramos administrativos.<br>
AUMENTOS DE GASTO SOCIAL:<br>
1. Se otorgaría un aumento salarial a los trabajadores del Estado de 18 por ciento, lo cual implicaría un incremento presupuestal de 3 mil 750 millones de pesos a las partidas correspondientes.<br>
2. Se implantaría el programa de apoyo a deudores pequeños y medianos, presentado por el Partido de la Revolución Democrática, por un monto de 9 mil millones de pesos para el año de 1999, en lugar de los cuatro mil millones pactados por el gobierno y el PAN.<br>
3. Se incrementaría el gasto educativo, incluyendo educación superior, por un total adicional de 10 mil millones de pesos.<br>
4. Se ampliarían los programas de desarrollo agropecuario con un monto adicional de tres mil 983 millones de pesos.<br>
5. Se ampliarían los fondos de desarrollo social en 4 mil millones de pesos adicionales.<br>
6. Se incrementarían las pensiones de jubilados y pensionados, así como de viudas de éstos, con mil 800 millones adicionales a lo ya programado.<br>
<br>
<br>
La propuesta del PRD no admite el incremento de la deuda pública pues el cálculo del Presidente Zedillo, en el sentido de que el déficit previsto para 1999 es de 1.25 puntos del Producto Interno Bruto, es engañoso, ya que si se toma en cuenta todo el financiamiento neto del sector público, incluso la intermediación financiera y los contratos de obras, dicho déficit supera los cuatro puntos del PIB.<br>
<br>
El programa de austeridad del PRD se refiere a lo siguiente:<br>
1. Se eliminan las cantidades no justificadas incluidas en la partida 1500, de servicios personales, de los altos funcionarios (director general hasta Presidente), con lo que se ahorran 5 mil 850 millones.<br>
2. Se recortan los gastos de materiales y suministros, que incluyen nueve partidas: alimentos y gasolina exclusivamente de altos funcionarios, mercancías diversas (abarrotes, muebles, ropa, línea blanca, perfumería, regalos, vajillas, etc., por 539.7 millones de pesos de las secretarías de Gobernación y Hacienda), en un total de mil 637.7 millones de pesos.<br>
3. Se recortan los gastos de servicios generales en 3 mil 955 millones, los cuales incluyen asesorías, viajes, difusión y "servicios oficiales".<br>
4. Se recortan las erogaciones de compra de automóviles para el uso de los altos funcionarios y nuevos equipos de comunicaciones y cómputo de oficinas (entre el 25 y el 50 por ciento), en un total de mil 926 millones de pesos.<br>
5. Se recorta a la Secretaría de Comunicaciones y Transportes un total de 2 mil 150 millones de pesos de proyectos de obra pública que no se encuentran justificados en el detalle de programas, así como las transferencias entre centros regionales de dicha secretaría, cuyo propósito no se incluye en el proyecto presentado por el Ejecutivo.<br>
6. Se realiza un recorte de "otros gastos de capital", por 688.5 millones, que se usan para "compra de valores", por parte de la Secretaría de Hacienda y gastos semejantes de otras secretarías, que no son prioritarios.<br>
7. Se realizan otros recortes menores de actividades que no se justifican.<br>
<br>
El Grupo Parlamentario del Partido de la Revolución Democrática presenta al pueblo esta propuesta y demandará, asimismo, que sea discutida en las comisiones de Hacienda y Presupuesto de la Cámara de Diputados. El PRD rechaza que las finanzas públicas del país se debatan en secreto, fuera de los recintos legislativos, y exige la discusión abierta y pública de tan importante asunto nacional.<br>
<br></P>
<br>
Palacio Legislativo, 23 de diciembre de 1998.</BLOCKQUOTE>
