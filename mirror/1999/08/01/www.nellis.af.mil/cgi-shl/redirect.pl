    <p><font face="Arial"><b>House committee cuts Raptor dollars, focuses on
    airmen</b> -- As Congress recommends slicing funds from the F-22 Raptor
    program, the Air Force is standing fast on its view that obtaining the
    next-generation air superiority fighter remains a necessity.</font></p>
    <p><font face="Arial"><b>Reconstitution efforts won't impact readiness</b>
    -- The service's senior military leader is emphasizing a break for weary
    troops but insists the Air Force is still ready to fight today if needed.</font></p>
    <p><font face="Arial"><b>Medical Service launches TRICARE information
    campaign</b> -- The Air Force Medical Service is making sure Air Force
    leaders are better informed about TRICARE, the health care plan for the
    uniformed services.&nbsp;</font></p>
    <p><font face="Arial"><b>Cohen, Shelton Rap Times' Editorial on Anthrax
    Shots </b>-- Halting mandatory anthrax shots would represent a
    &quot;significant disservice&quot; to men and women in uniform, Defense
    Secretary William Cohen and Gen. Henry Shelton said in a written rebuttal to
    the Army Times Publishing Co.</font></p>
    <p><font face="Arial"><b>Air Force helps search for JFK, Jr.</b> -- When
    search and rescue efforts for a potential downed aircraft with John F.
    Kennedy, Jr., his wife and his sister-in-law on board commenced, the Air
    Force swiftly moved into action.</font></p>
    <p><font face="Arial"><b>SECAF confirmation hearing today</b> -- Acting
    Secretary of the Air Force F. Whitten Peters is scheduled to testify today
    before members of the Senate Armed Services Committee during his
    confirmation hearing as President Clinton's nominee for secretary of the Air
    Force. Mr. Peters has served as the under secretary and acting secretary of
    the Air Force since November 1997. He was nominated as secretary of the Air
    Force June 17.</font></p>
    <p><font face="Arial"><b>AF cyclists pedal across Iowa</b> -- Although there
    are no Air Force bases in Iowa, the service will be well represented by 120
    cycling enthusiasts participating in a 7-day bicycle ride across the state
    beginning Sunday. The annual 530-mile ride, known as The Register's Annual
    Great Bicycle Ride Across Iowa, attracts close to 20,000 riders. Most of the
    Air Force participants have logged more than 1,000 miles training for the
    event which involves rolling countryside and heat indices of nearly 125
    degrees.</font></p>
    <p><font face="Arial"><b>New EQUAL list due Aug. 9</b> -- The newest
    Enlisted Quarterly Assignment Listing of available enlisted assignments
    overseas will be available at military personnel flights and on the Air
    Force Personnel Center's World Wide Web home page Aug. 9.</font></p>
