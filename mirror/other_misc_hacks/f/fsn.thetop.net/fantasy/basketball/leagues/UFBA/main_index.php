<H2>Sandstorm Win</H2>
<TABLE>
<TR>
<TD width="150" valign=top>
<IMG SRC="images/duncan2.jpg" HEIGHT=250 WIDTH=150><BR>
<FONT SIZE=-2><B>
Tim Duncan scored 65 TFP for the week to lead the Sandstorm in the finals and
win the MVP award.
</TD><TD valign=top>
The Sandstorm (18-4) beat the Arsenal (17-5) in the UFBA Finals to win their
second championship 149-111.  Tim Duncan lead the Sandstorm with 65 TFP for the week, and overcame an early injury to Tracy McGrady that limited him to 8 TFP for one game during the week. "We were worried about losing Tracy," said coach Hoy of the Sandstorm. "When he went down I thought it was over, but he came back strong."  McGrady finished with a solid 42 TFP contribution.
<P>
Coach Brown of the Arsenal said, "I knew McGrady wasn't out for the game.  This is was the players live for and he was going to drag himself back onto the court somehow."  The Arsenal were hurt by an foot injury to surprise starter Toni Kukoc, who scored only 17 TFP for the week.  "It was a risk/reward decision to start Toni.  With how hot the Sandstorm have been we knew we'd have to have a huge week from his position to win.  It was a risk, but the reward could have been huge," said Brown.  In fact, the team would have still lost 145-130 with Glen Robinson in the lineup instead of Kukoc, even with Robinson's 50 TFP.
<P>
Duncan won MVP honors and was joined on the All-touney team by:<BR>
Vince Carter, Sandstorm<BR>
Shareef Abdur-Rahim, Arsenal<BR>
Jason Kidd, Nanooks <BR>
Kevin Garnett, Ice 
