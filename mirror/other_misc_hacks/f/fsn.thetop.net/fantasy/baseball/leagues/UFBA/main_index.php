<H1>Sandstorm Fall</H1>
<TABLE>
<TR>
<TD valign=top>
Then there was one... <P>
The Sandstorm (4-1) fell from the ranks of the unbeaten as they were upset big by the Austinites (3-2) in week 5.  The Austinites lit up the scoreboard beating the Sandstorm by nearly 100 points, 268-179.  "We're an up and down team," said Austinites coach Michael Casson, "but we're always going to be in the big games." 
<P>
The Mountaineers (5-0) are the only undefeated team left in the UFBA after an unimpressive 217-128 win over the last place Hoosiers (0-5) who are yet to win.  The Mountaineers face their first test of the season as they face off against the 3rd ranked Arsenal (4-1) in week 6.  The Arsenal came back after trailing the entire week and beat a pesky Turbulence (1-4) team.  "That team is a lot better than their record indicates," said Arsenal Coach Matt Brown.  "Mark my words, they will be in the playoffs."
<P>
For the second week in a row the leagues best pitcher and batter were on the same team.  The Nightmare (2-3) won impressively over the Lous 253-212.  The team was led by Pitcher of the Week, Greg Maddux who had 28 TFP in his one start.  Maddux went 9 innings to get the win and struck out 14 while not allowing a run.  Raul Mondesi also was a key to the win hitting 46 TFP for the week.  Mondesi .464 with 4 HRs, 11 RBI and 8 runs scored.
